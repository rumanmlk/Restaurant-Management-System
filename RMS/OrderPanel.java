    private void populateTableFromDatabase() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0); // Clear existing data from the table

        try {
            statement = connection.createStatement();
            resultSet = statement.executeQuery("SELECT * FROM `Order`");

            while (resultSet.next()) {
                int orderId = resultSet.getInt("ID");
                int customerId = resultSet.getInt("CustomerID");
                int deliveryBoyId = resultSet.getInt("DeliveryBoyID");

                Timestamp orderDate = resultSet.getTimestamp("OrderDate");
                Timestamp deliveryDate = resultSet.getTimestamp("DeliveryDate");

                double totalAmount = resultSet.getDouble("TotalAmount");

                String status = resultSet.getString("Status");
                String paymentMethod = resultSet.getString("PaymentMethod");

                Object[] rowData = {
                    orderId,
                    customerId,
                    deliveryBoyId,
                    orderDate, 
                    deliveryDate, 
                    customerId, 
                    totalAmount,
                    status,
                    paymentMethod
                };
                model.addRow(rowData);
             }
            
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error occurred while fetching data from the database.");
        } finally {
            try {
                // Close the ResultSet, Statement, and Connection
                if (resultSet != null) resultSet.close();
                if (statement != null) statement.close();
                if (connection != null) connection.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
