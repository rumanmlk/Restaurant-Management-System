    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String age = jTextField6.getText();
        int Age = Integer.parseInt(age);
        String phone = jTextField2.getText();
        String email = jTextField3.getText();
        String address = jTextField4.getText();
        String cnic = jTextField5.getText();
        String role = jComboBox1.getSelectedItem().toString();
        String paygrade = "e";
        String username = jTextField7.getText();
        String password = jTextField8.getText();
        connectToDatabase();

        String query = "INSERT INTO Staff (Name, Age, Phone, Email, Address, CNIC, Role, Paygrade, Username, Password) VALUES (?, ?, ?, ?, ?, ?,?,?,?,?)";

            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, name);
                preparedStatement.setInt(2, Age);
                preparedStatement.setString(3, phone);
                preparedStatement.setString(4, email);
                preparedStatement.setString(5, address);
                preparedStatement.setString(6, cnic);
                preparedStatement.setString(7, role);
                preparedStatement.setString(8, paygrade);
                preparedStatement.setString(9, username);
                preparedStatement.setString(10, password);

                int rowsAffected = preparedStatement.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Item inserted successfully!");
                    StaffPanel Panel = new StaffPanel();
                    Panel.setVisible(true);
                    this.dispose();
                }
                SQLWarning warning = preparedStatement.getWarnings();
                if (warning != null) {
                    System.err.println("SQL Warning: " + warning.getMessage());
                }
            } catch (SQLException e) {
                System.err.println("SQL Exception: " + e.getMessage());
                e.printStackTrace();
                if (e.getNextException() != null) {
                    System.err.println("Next Exception: " + e.getNextException().getMessage());
                }
                JOptionPane.showMessageDialog(null, "Please check your entries for proper data types!!");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

