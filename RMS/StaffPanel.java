    private void populateTableFromDatabase() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        
        model.setRowCount(0); // Clear existing data from the table

        try {
            statement = connection.createStatement();
            resultSet = statement.executeQuery("SELECT * FROM Staff");

            while (resultSet.next()) {
                int orderId = resultSet.getInt("ID");
                String customerId = resultSet.getString("Name");
                int deliveryBoyId = resultSet.getInt("Age");

                String orderDate = resultSet.getString("Phone");
                String deliveryDate = resultSet.getString("Email");

                String totalAmount = resultSet.getString("Address");

                String status = resultSet.getString("CNIC");
                String paymentMethod = resultSet.getString("Role");
                 String pay = resultSet.getString("Paygrade");

                String username = resultSet.getString("Username");

                String password = resultSet.getString("Password");


                Object[] rowData = {
                    orderId,
                    customerId,
                    deliveryBoyId,
                    orderDate, 
                    deliveryDate, 
                    customerId, 
                    totalAmount,
                    status,
                    paymentMethod,
                    pay,
                    username,
                    password
                };
                model.addRow(rowData);
             }
            
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error occurred while fetching data from the database.");
        } finally {
            try {
                // Close the ResultSet, Statement, and Connection
                if (resultSet != null) resultSet.close();
                if (statement != null) statement.close();
                if (connection != null) connection.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
