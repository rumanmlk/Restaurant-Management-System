    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int[] selectedRows = jTable2.getSelectedRows();
        DefaultTableModel model = (DefaultTableModel) jTable2.getModel();

        try {
        connectToDatabase();
            
            for (int i = selectedRows.length - 1; i >= 0; i--) {
                int modelRowIndex = jTable2.convertRowIndexToModel(selectedRows[i]);
                int idToDelete = (int) model.getValueAt(modelRowIndex, 0); 
                System.out.println(idToDelete);

                // Delete row from the database using the ID
                PreparedStatement preparedStatement = connection.prepareStatement("DELETE FROM AreaCode WHERE ID = ?");
                preparedStatement.setInt(1, idToDelete);
                preparedStatement.executeUpdate();
            }

            connection.commit(); // Commit the transaction
            
            // Remove selected rows from the table model
            for (int i = selectedRows.length - 1; i >= 0; i--) {
                model.removeRow(selectedRows[i]);
            }
        } catch (SQLException ex) {
            try {
                connection.rollback(); // Rollback the transaction if there's an error
            } catch (SQLException rollbackEx) {
                rollbackEx.printStackTrace();
            }
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Error occurred while deleting rows from the database.");
        } finally {
            try {
                connection.setAutoCommit(true); // Reset auto-commit to true
            } catch (SQLException autoCommitEx) {
                autoCommitEx.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

