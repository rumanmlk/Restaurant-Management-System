    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String name = jTextField1.getText();
        String description = jTextField2.getText();
        BigDecimal price = new BigDecimal(jTextField3.getText());
        String image = jTextField4.getText();
        String category = jTextField5.getText();
        System.out.println(name);
        System.out.println(description);
         System.out.println(price);
        System.out.println(image);
        System.out.println(category);
        connectToDatabase();

            String query = "INSERT INTO FoodItem (Name, Description, Price, Image, Category, Availability) VALUES (?, ?, ?, ?, ?, 1)";

            try (PreparedStatement preparedStatement = connection.prepareStatement(query)) {
                preparedStatement.setString(1, name);
                preparedStatement.setString(2, description);
                preparedStatement.setBigDecimal(3, price);
                preparedStatement.setString(4, image);
                preparedStatement.setString(5, category);

                int rowsAffected = preparedStatement.executeUpdate();

                if (rowsAffected > 0) {
                    JOptionPane.showMessageDialog(null, "Item inserted successfully!");
                    InventoryPanel Inventory = new InventoryPanel();
                    Inventory.setVisible(true);
                    this.dispose();
                }
                SQLWarning warning = preparedStatement.getWarnings();
                if (warning != null) {
                    System.err.println("SQL Warning: " + warning.getMessage());
                }
            } catch (SQLException e) {
                System.err.println("SQL Exception: " + e.getMessage());
                e.printStackTrace();
                if (e.getNextException() != null) {
                    System.err.println("Next Exception: " + e.getNextException().getMessage());
                }
                JOptionPane.showMessageDialog(null, "Please check your entries for proper data types!!");
            }


    }//GEN-LAST:event_jButton1ActionPerformed

