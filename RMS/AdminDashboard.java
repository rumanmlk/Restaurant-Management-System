    private void populateTableFromDatabase() {

        try {
            statement = connection.createStatement();
            resultSet = statement.executeQuery("SELECT * FROM adminSummary");

            while (resultSet.next()) {
                Object[] rowData = {
                    resultSet.getInt("CustomerCount"),
                    resultSet.getInt("DeliveryBoyCount"),
                    resultSet.getInt("FoodItemCount"),
                    resultSet.getInt("OrderCount"),
                    resultSet.getInt("ReviewCount"),
                };
                for (Object data : rowData) {
                    System.out.println(data);
                }
                jLabel11.setText(String.valueOf(rowData[0]));
                jLabel13.setText(String.valueOf(rowData[1]));
                jLabel15.setText(String.valueOf(rowData[3]));
                jLabel17.setText(String.valueOf(rowData[2]));
                jLabel19.setText(String.valueOf(rowData[4]));
            }
            
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error occurred while fetching data from the database.");
        } finally {
            try {
                // Close the ResultSet, Statement, and Connection
                if (resultSet != null) resultSet.close();
                if (statement != null) statement.close();
                if (connection != null) connection.close();
            } catch (SQLException e) {
                e.printStackTrace();
            }
        }
    }
